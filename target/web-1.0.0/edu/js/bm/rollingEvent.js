function rollingController(data, id, elClass){
    this.$listWrap = null;
    this.$listClone = null;
    this.$itemWrap = null;
    this.init(data, id, elClass);
};
rollingController.prototype = {
    init : function(data, id, elClass){
        this.$listWrap = $('#'+id);
        this.$listClone = this.$listWrap.find('.'+elClass).clone();
        this.$itemWrap = $('<div class="col-12 swiper-wrapper"></div>');
        this.setEventDom(data, id)
    }
    , setEventDom : function( data, id ){
        this.$listWrap.html('');
        var currIdx = 0;
        for( var i = 0 ; i < data.length; i++ ){
            if(this.$itemWrap.find('.swiper-slide').length == 3) continue;
            var $itemCon = this.$listClone.clone();
            if( data[i]["img"] != null && data[i]["img"] !== undefined )$itemCon.find('[name="img"]').attr('src', data[i]["img"]);
            if( data[i]["title"] != null && data[i]["title"] !== undefined )$itemCon.find('[name="title"]').html(data[i]["title"]);
            if( data[i]["content"] != null && data[i]["content"] !== undefined )$itemCon.find('[name="content"]').html(data[i]["content"]);
            if( data[i]["startDate"] != null && data[i]["startDate"] !== undefined )$itemCon.find('[name="startDate"]').html(data[i]["startDate"]);
            if( data[i]["endDate"] != null && data[i]["endDate"] !== undefined )$itemCon.find('[name="endDate"]').html(data[i]["endDate"]);
            if( data[i]["compName"] != null && data[i]["compName"] !== undefined )$itemCon.find('[name="compName"]').html(data[i]["compName"]);
            if( data[i]["careersDate"] != null && data[i]["careersDate"] !== undefined )$itemCon.find('[name="careersDate"]').html(data[i]["careersDate"]);
            if( data[i]["enTitle"] != null && data[i]["enTitle"] !== undefined )$itemCon.find('[name="enTitle"]').html(data[i]["enTitle"]);
            if( data[i]["krTitle"] != null && data[i]["krTitle"] !== undefined )$itemCon.find('[name="krTitle"]').html(data[i]["krTitle"]);
            if( data[i]["btnName"] != null && data[i]["btnName"] !== undefined )$itemCon.find('[name="btnName"]').html(data[i]["btnName"]);
            if( data[i]["content"] != null && data[i]["content"] !== undefined )$itemCon.find('[name="content"]').html(data[i]["content"]);
            this.$itemWrap.append($itemCon);

        }
        var str = {'width':this.$listWrap.width()+'px'};
        if(id == 'offjt_body_con' || id == 'subject_body_con') str['display']='block';

        this.$itemWrap.css(str);
        this.$listWrap.append(this.$itemWrap);
        this.$listWrap.append('<div class="col-12 swiper-pagination"></div>');
    }
};


